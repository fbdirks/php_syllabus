<h2>Functies</h2>
<p>Functies zijn taken die willekeurig vaak door de hoofdcode kunnen worden uitgevoerd maar die op &eacute;&eacute;n plek buiten de hoofdcode geprogrammeerd en onderhouden worden.</p>
<p>Voor de programmeur kunnen functies lijken op een soort zwarte doos waar je wat gegevens in stopt en die het passende antwoord daarop geeft. Een voorbeeld is het <b>sqrt</b> commando dat de wortel van een getal oplevert. Als je:
	<pre>$wortel = sqrt(144);</pre>
zal in de variabele $wortel 12 stoppen.
	 </p>
<p>PHP heeft duizenden eigen, ingebouwde functies. Op <a href="http://php.net" target="_new">PHP.net</a> kun je naar die functies zoeken en de documentatie ervan raadplegen. Maak je gebruik van extra PHP bibliotheken voor speciale functies (bijvoorbeeld een editor of beeldbewerkingsfuncties) dan vind je de documentatie natuurlijk bij de leveranciers van die functies.</p>
</p>
