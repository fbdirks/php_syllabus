<h1>les 1: Introductie: PHP & MySQL</h1>
<p>In de volgende lessen gaan we PHP en MySQL combineren. Dat is een sterkte combinatie omdat je daarmee databases kunt koppelen aan websites. Denk aan een inlogsysteem waarbij je je gebruikers (en hun gegevens) opslaat in een database, denk aan forumsystemen of webshops, de mogelijkheden zijn eindeloos.</p>
<p>PHP heeft verschillende <em>extensies</em> standaard ingebouwd zitten die de communicatie tussen PHP en een MySQL database kunnen regelen:</p>
<ul>
	<li>De standaard mysql-extensie</li>
	<li>De nieuwere mysqli-extensie</li>
	<li>De PDO-extensie</li>
</ul>
<p>De standaard mysql-extensie wordt in het boek uitgelegd, maar is verouderd. De betrouwbaarheid en zekerheid ervan is niet meer up to date en daarom wordt deze extensie binnenkort in PHP zelfs uitgeschakeld. De mysqli extensie is nieuwer en beter, en <strong>die</strong> staat in de volgende lessen centraal. De PDO-extensie hoort bij Object Georienteerd programmeren. In de volgende lessen wordt deze niet besproken, maar als je hem wilt en kunt gebruiken mag dat bij het uitwerken van opgaven wel. Maar documenteer dan wel precies wat je doet.</p>
<p>In de bespreking van de mysqli extensie zullen in eerste instantie voorbeelden gebruikt worden van de <em>procedurele methode</em>. W3Schools noemt dat bijvoorbeeld <strong>MySQLi Procedural</strong> in hun voorbeelden. Geprobeerd zal worden om de voorbeelden ook uit te werken in de Object-georienteerde stijl. </p>
